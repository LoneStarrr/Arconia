package lonestarrr.arconia.client.integration.jei;

import com.mojang.blaze3d.systems.RenderSystem;
import lonestarrr.arconia.common.Arconia;
import lonestarrr.arconia.common.block.ModBlocks;
import mezz.jei.api.constants.VanillaTypes;
import mezz.jei.api.gui.builder.IRecipeLayoutBuilder;
import mezz.jei.api.gui.drawable.IDrawable;
import mezz.jei.api.gui.ingredient.IRecipeSlotsView;
import mezz.jei.api.helpers.IGuiHelper;
import mezz.jei.api.recipe.IFocusGroup;
import mezz.jei.api.recipe.RecipeIngredientRole;
import mezz.jei.api.recipe.category.IRecipeCategory;
import mezz.jei.api.recipe.types.IRecipeType;
import net.minecraft.client.gui.GuiGraphics;
import net.minecraft.network.chat.Component;
import net.minecraft.resources.ResourceLocation;
import net.minecraft.world.item.ItemStack;

import javax.annotation.Nonnull;

/** Virtual recipe category for showing all items that are generated by the pot of gold
 */
public class ImbuedBranchRecipeCategory implements IRecipeCategory<ImbuedBranchRecipe> {
    private final IDrawable overlay;
    private final IDrawable icon;
    private final ItemStack renderStack = new ItemStack(ModBlocks.hat.get().asItem());

    public static final IRecipeType<ImbuedBranchRecipe> TYPE =
            IRecipeType.create(Arconia.MOD_ID, "imbued_branch", ImbuedBranchRecipe.class);

    public ImbuedBranchRecipeCategory(@Nonnull IGuiHelper guiHelper) {
        this.overlay = guiHelper.createDrawable(ResourceLocation.fromNamespaceAndPath(Arconia.MOD_ID, "textures/gui/jei/imbued_branch_overlay.png"),
                0, 0, 144, 81);
        this.icon = guiHelper.createDrawableIngredient(VanillaTypes.ITEM_STACK, renderStack.copy());
    }
    @Override
    public IRecipeType<ImbuedBranchRecipe> getRecipeType() {
        return TYPE;
    }

    @Override
    public Component getTitle() {
        return Component.translatable("jei.arconia.recipe_category.imbued_branch");
    }

    @Override
    public int getWidth() {
        return 144;
    }

    @Override
    public int getHeight() {
        return 81;
    }

    @Override
    public IDrawable getIcon() {
        return this.icon;
    }

    @Override
    public void draw(ImbuedBranchRecipe recipe, IRecipeSlotsView recipeSlotsView, GuiGraphics gui, double mouseX, double mouseY) {
        RenderSystem.enableBlend();
        overlay.draw(gui, 0, 0);
        RenderSystem.disableBlend();

    }

    @Override
    public void setRecipe(IRecipeLayoutBuilder builder, ImbuedBranchRecipe recipe, IFocusGroup focuses) {
        ItemStack branch = recipe.getColoredBranch();
        ItemStack generated = recipe.getOutput();
        builder.addSlot(RecipeIngredientRole.INPUT, 33, 33).add(branch);
        builder.addSlot(RecipeIngredientRole.OUTPUT, 121, 33).add(generated);
    }
}
